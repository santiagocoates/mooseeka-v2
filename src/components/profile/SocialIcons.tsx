interface SocialLinksProps {
  socials: Record<string, string>
}

export function SocialLinks({ socials }: SocialLinksProps) {
  const active = SOCIAL_CONFIG.filter(s => socials[s.id]?.trim())
  if (active.length === 0) return null

  return (
    <div className="flex items-center gap-2 flex-wrap">
      {active.map(s => (
        <a
          key={s.id}
          href={s.buildUrl(socials[s.id])}
          target="_blank"
          rel="noopener noreferrer"
          title={s.label}
          className="group w-8 h-8 rounded-full flex items-center justify-center transition-all hover:scale-110 active:scale-95"
          style={{
            background: 'rgba(255,255,255,0.06)',
            border: '1px solid rgba(255,255,255,0.1)',
            color: '#7A6890',
          }}
          onMouseOver={e => {
            e.currentTarget.style.background = `${s.color}22`
            e.currentTarget.style.borderColor = `${s.color}55`
            e.currentTarget.style.color = s.color
          }}
          onMouseOut={e => {
            e.currentTarget.style.background = 'rgba(255,255,255,0.06)'
            e.currentTarget.style.borderColor = 'rgba(255,255,255,0.1)'
            e.currentTarget.style.color = '#7A6890'
          }}
        >
          {s.icon}
        </a>
      ))}
    </div>
  )
}
